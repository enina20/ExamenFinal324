<h2>INFORMACION DE REQUISITOS</h2>
<h3>Documentos Necesarios:</h3>
<ul>
    <li>Formulario de inscripción completo</li>
    <li>Copia del documento de identidad (DNI, cédula, pasaporte, etc.)</li>
    <li>Certificado de estudios secundarios o equivalente</li>
    <li>Comprobante de pago de matrícula</li>
</ul>

<h3>Requisitos Académicos:</h3>
<ul>
    <li>Haber aprobado los cursos previos requeridos como requisito</li>
    <li>Contar con el nivel de conocimiento necesario para el curso</li>
    <li>No tener deudas pendientes con la universidad</li>
    <li>Mantener un promedio académico mínimo establecido</li>
</ul>